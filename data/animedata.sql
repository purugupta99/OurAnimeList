drop table if exists anime;

CREATE TABLE anime(
RANK INTEGER,
POPULARITY INTEGER,
SCORE REAL,
NAME_ENG TEXT primary key NOT NULL,
SYNONYMS TEXT,
NAME_JAP TEXT,
CAST_TYPE TEXT,
EPISODES_NUM INTEGER,
STATUS TEXT,
AIRED TEXT,
PREMIERE TEXT,
BROADCAST TEXT,
PRODUCERS TEXT,
LICENSORS TEXT,
STUDIOS TEXT,
SOURCES TEXT,
GENRES TEXT,
DURATION TEXT,
RATING TEXT,
SYNOPSIS TEXT,
START_THEME TEXT,
END_THEME TEXT);

drop table if exists characters;

CREATE TABLE characters(
NAME_ENG TEXT,
URL_CHAR TEXT,
NAME_CHAR TEXT,
ROLE TEXT,
URL_ACTOR TEXT,
NAME_ACTOR TEXT,
NATIONALITY TEXT,
FOREIGN KEY(NAME_ENG) references anime(NAME_ENG));

drop table if exists anime_genre;

CREATE TABLE anime_genre(
NAME_ENG TEXT,
GENRES TEXT,
FOREIGN KEY(NAME_ENG) references anime(NAME_ENG));